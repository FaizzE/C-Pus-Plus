#include<bits/stdc++.h>
using namespace std;
int main()
{
    // Bitwise Operators
    // bits on of number (integers,chars)
    // & = bitwise and
    // | = bitwise or
    // ^ = bitwise xor
    // ~ = bitwise 1's compliment
    // << = left shift
    // >> = right shift


    // Bitwise AND
    cout << (5 & 7) << endl;
    cout << (5 & 2) << endl;

    // bitwise OR
    cout << (5 | 7) << endl; // 7
    cout << (5 | 3) << endl; // 7

    // Bitwise XOR (Explucive OR)
    cout << (5 ^ 7) << endl; // 2
    cout << (5 ^ 5) << endl; // 0

    // Bitwise Not
    cout << (~0) << endl; 


    return 0;
}